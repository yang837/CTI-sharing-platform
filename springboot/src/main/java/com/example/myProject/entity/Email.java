package com.example.myProject.entity;

public class Email {
    private int id;
    private String number;
    private String account;
    private String md5;
    private String time;

    public int getId() {
        return id;
    }

    public String getNumber() {
        return number;
    }

    public String getAccount() {
        return account;
    }

    public String getMd5() {
        return md5;
    }

    public String getTime() {
        return time;
    }
}
